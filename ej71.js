// Mostrar en consola la tabla de multiplicar del 9 (de 1 a 10) utilizando la estructura while

let contador = 1; 
const numero = 9; 

while (contador <= 10) {
    let resultado = numero * contador; 
    console.log(`${numero} x ${contador} = ${resultado}`); 
    contador++; }

    